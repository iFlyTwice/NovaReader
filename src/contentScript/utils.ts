// Keyboard shortcuts
export const KEYBOARD_SHORTCUTS = {
  TOGGLE_PANEL: 'Alt+R',
  TOGGLE_PLAYER: 'Alt+P'
};

// Event handling
export function addKeyboardShortcuts(
  togglePanel: () => void,
  togglePlayer: () => void
): void {
  document.addEventListener('keydown', (event) => {
    // Alt+R to toggle the panel
    if (event.altKey && event.key === 'r') {
      togglePanel();
    }
    // Alt+P to toggle the side player
    if (event.altKey && event.key === 'p') {
      togglePlayer();
    }
  });
}
